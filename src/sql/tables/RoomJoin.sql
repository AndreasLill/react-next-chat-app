CREATE TABLE [dbo].[RoomJoin] (
    [RoomID] UNIQUEIDENTIFIER FOREIGN KEY REFERENCES [dbo].[Room]([ID]) ON DELETE CASCADE,
    [UserID] UNIQUEIDENTIFIER FOREIGN KEY REFERENCES [dbo].[User]([ID]) ON DELETE CASCADE,
    [JoinedDate] DATETIME DEFAULT(CURRENT_TIMESTAMP) NOT NULL,
	[Role] INT NOT NULL, 
	PRIMARY KEY (RoomID, UserID)
)